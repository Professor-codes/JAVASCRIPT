let arr = [1, 2, 3, 4, 5];

// push element
arr.push(6);
console.log(`push -> ${arr}`); // add 6 to ending point of an arr

// pop element
arr.pop(); // remove last element from arr
console.log(`pop -> ${arr}`);

// unshift element
arr.unshift(0);
console.log(`unshift -> ${arr}`); // add 0 to starting point of an arr

// shift element
arr.shift();
console.log(`shift -> ${arr}`); // remove first element from arr

// array length
console.log(`arr length -> ${arr.length}`);