// ARITHMETIC OPERATOR
let a = 10;
let b = 5;

console.log(a + b); // Addition -> 15
console.log(a - b); // Subtraction -> 5
console.log(a * b); // Multiplication -> 50
console.log(a / b); // Division -> 2
console.log(a % b); // Modulus -> 0

// INCREMENT OPERATOR
let i = 10;
console.log(i++);   // Post Increment -> 10 (a is now 10)
i = 10;
console.log(++i);   // Pre Increment -> 11 (a is now 11)
let j = 5;
console.log(j--)    // Post Decrement -> 5 (b is now 5)
j = 5;
console.log(--b);   // Pre Decrement -> 4 (b is now 4)
