// NON PRIMITIVE DATA TYPES

// OBJECT

let details = {
    name: "Alex",
    age: 25,
    designation: "Content Creator",
}

// PRINT WHOLE OBJECT
console.log(details);

// DOT METHOD TO ACCESS OBJECT
// PRINT PROPERTIES OF OBJECT
console.log(`Name : ${details.name}`);
console.log(`Age : ${details.age}`);
console.log(`Designation : ${details.designation}`);

// BRACKET METHOD TO ACCESS OBJECT
// PRINT PROPERTIES OF OBJECT
console.log(`Name : ${details["name"]}`);
console.log(`Age : ${details["age"]}`);
console.log(`Designation : ${details["designation"]}`);