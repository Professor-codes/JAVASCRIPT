// IF
let age = 20;
if (age >= 18) {
    console.log("You are an adult.");
}

// IF ELSE
if (age <= 18) {
    console.log("You are an adult.");
} else {
    console.log("You are a minor.");
}

// ELSE IF
let score = 85;
if (score >= 90) {
    console.log("Grade: A");
} else if (score >= 80) {
    console.log("Grade: B");
} else if (score >= 70) {
    console.log("Grade: C");
} else {
    console.log("Grade: D or below");
}

