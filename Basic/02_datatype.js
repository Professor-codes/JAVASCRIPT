// PRIMITIVE DATA TYPES

// BNSU short for BBNNSSU

// BOOLEAN
let check = true;
console.log(check);
// BIGINT
let num = BigInt(12345)
console.log(num);
// NULL
let value = null;
console.log(value);
// NUMBER
let count = 90;
console.log(count);
// STRING
let name = 'masha';
console.log(name);
// SYMBOL
let brand = Symbol('This is masha');
console.log(brand);
// UNDEFINED
let condition = undefined;
console.log(condition);